// C++ Program to Find Size of int, float, double and char
#include<iostream>
using namespace std;
main()
{
    int a;
    float b;
    double d;
    char c;
    cout<<"size of int is "<<sizeof(a)<<"\n";
    cout<<"size of float is  "<<sizeof(b)<<"\n";
    cout<<"size of double is "<<sizeof(d)<<"\n";
    cout<<"size of character is  "<<sizeof(c)<<"\n";
}